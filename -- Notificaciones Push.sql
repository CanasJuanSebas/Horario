-- Que hace este archivo:
--   1) Tabla para guardar los dispositivos que activaron notificaciones.
--   2) Un lugar privado (no accesible desde el navegador) para guardar
--      la URL de la Edge Function y un secreto compartido con ella.
--   3) Una funcion que sabe "en que semana y que dia estamos" (igual
--      que el codigo del navegador) para usarla desde la base de datos.
--   4) Un trigger: cuando se guarda o cambia una tarea, se avisa.
--   5) pg_cron: a la hora exacta de cada clase, si hay tarea, se avisa.
--   6) pg_cron: todos los dias a las 5:30am (hora Bogota) se manda el
--      resumen de tareas del dia.
--
-- IMPORTANTE: antes de correr esto, despliega la Edge Function
-- "send-push" (ver README-notificaciones.md) y ten a la mano:
--   - La URL de tu Edge Function, algo como:
--     https://TU_PROYECTO.functions.supabase.co/send-push
--   - Un secreto (cualquier texto largo y aleatorio) que tambien
--     configures como variable de entorno CRON_SECRET en la funcion.
-- ============================================================

-- 1) Suscripciones push (una fila por dispositivo/navegador) ---------
create table if not exists public.push_subscriptions (
  id bigint generated by default as identity primary key,
  endpoint text unique not null,
  p256dh text not null,
  auth text not null,
  created_at timestamptz default now()
);

alter table public.push_subscriptions enable row level security;

drop policy if exists "anon puede suscribirse" on public.push_subscriptions;
create policy "anon puede suscribirse"
  on public.push_subscriptions for insert
  to anon, authenticated
  with check (true);

drop policy if exists "anon puede actualizar su suscripcion" on public.push_subscriptions;
create policy "anon puede actualizar su suscripcion"
  on public.push_subscriptions for update
  to anon, authenticated
  using (true)
  with check (true);

drop policy if exists "anon puede darse de baja" on public.push_subscriptions;
create policy "anon puede darse de baja"
  on public.push_subscriptions for delete
  to anon, authenticated
  using (true);
-- (Sin politica de SELECT para anon: nadie puede listar los suscritos
--  de otras personas desde el navegador; solo la Edge Function, que
--  usa la service_role key, puede leerlos.)


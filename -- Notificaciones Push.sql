-- Extensiones necesarias para llamar HTTP desde Postgres y programar tareas
create extension if not exists pg_net with schema extensions;
create extension if not exists pg_cron with schema extensions;

-- Funcion que llama a la Edge Function (uso interno, no expuesta)
create or replace function private.notify_push(payload jsonb)
returns void
language plpgsql
security definer
set search_path = public, private, extensions
as $$
declare
  v_url text;
  v_secret text;
begin
  select value into v_url    from private.app_secrets where key = 'edge_function_url';
  select value into v_secret from private.app_secrets where key = 'cron_secret';

  if v_url is null or v_url like '%REEMPLAZA%' then
    raise notice 'notify_push: configura edge_function_url en private.app_secrets antes de usar notificaciones';
    return;
  end if;

  perform net.http_post(
    url := v_url,
    headers := jsonb_build_object('Content-Type', 'application/json', 'x-cron-secret', v_secret),
    body := payload
  );
end;
$$;
revoke all on function private.notify_push(jsonb) from public;

-- 3) Nombre de materia a partir de la celda (igual que MATS en el JS) --
create or replace function public.subject_name(p_cell_key text)
returns text language sql immutable as $$
  select case p_cell_key
    when '0-0' then 'Informática' when '0-1' then 'Matemáticas' when '0-2' then 'Física'    when '0-3' then 'Filosofía'
    when '1-0' then 'Robótica'    when '1-1' then 'Español'     when '1-2' then 'Español'    when '1-3' then 'Español'
    when '2-0' then 'Inglés'      when '2-1' then 'Física'      when '2-2' then 'Geometría'  when '2-3' then 'Inglés'
    when '3-0' then 'Política'    when '3-1' then 'Ética'       when '3-2' then 'Inglés'     when '3-3' then 'Español'
    when '4-0' then 'Matemáticas' when '4-1' then 'Ciencias'    when '4-2' then 'Física'     when '4-3' then 'Artística'
    when '5-0' then 'Inglés'      when '5-1' then 'Inglés'      when '5-2' then 'Sociales'   when '5-3' then 'Matemáticas'
    when '6-0' then 'Química'     when '6-1' then 'Química'     when '6-2' then 'Filosofía'  when '6-3' then 'Estadística'
    when '7-0' then 'Edu. Física' when '7-1' then 'Economía'    when '7-2' then 'Química'    when '7-3' then 'Religión'
    else (case when p_cell_key like 'mil-%' then 'Fase Militar' else 'Materia' end)
  end;
$$;

-- Semana actual, calculada igual que ANCHOR_WEEK/ANCHOR_MON en index.html
-- (semana 21 = lunes 6 de julio de 2026). Si cambias el ancla en el JS,
-- cambia tambien aqui.
create or replace function public.current_week_number()
returns int language sql stable as $$
  select greatest(1, 21 + floor(
    ((now() at time zone 'America/Bogota')::date - date '2026-07-06') / 7.0
  )::int);
$$;

-- Columna del dia de hoy: 0=Lunes ... 4=Viernes (5/6 = fin de semana)
create or replace function public.today_day_col()
returns int language sql stable as $$
  select (extract(isodow from (now() at time zone 'America/Bogota'))::int - 1);
$$;

-- 4) Trigger: nueva tarea o cambio en una tarea existente --------------
create or replace function public.trg_schedule_change()
returns trigger
language plpgsql
security definer
set search_path = public, private, extensions
as $$
declare
  v_subject text;
  v_is_new boolean;
  v_meaningful_change boolean;
begin
  if TG_OP = 'DELETE' then
    return old;
  end if;

  if NEW.task is null or btrim(NEW.task) = '' then
    return NEW; -- se vacio la celda, no notificar
  end if;

  v_is_new := (TG_OP = 'INSERT');
  v_meaningful_change :=
    v_is_new
    or (OLD.task is distinct from NEW.task)
    or (OLD.due_date is distinct from NEW.due_date)
    or (OLD.instructor is distinct from NEW.instructor);

  if not v_meaningful_change then
    -- Solo cambio de estado (pendiente/entregada/etc): no generar push
    -- para evitar avisos por cada clic de estado.
    return NEW;
  end if;

  v_subject := coalesce(public.subject_name(NEW.cell_key), 'Materia');

  perform private.notify_push(jsonb_build_object(
    'title', case when v_is_new then '📌 Nueva tarea: ' || v_subject else '✏️ Tarea actualizada: ' || v_subject end,
    'body', NEW.task || case when NEW.due_date is not null then (E'\nEntrega: ' || to_char(NEW.due_date, 'DD/MM/YYYY')) else '' end,
    'tag', 'task-' || NEW.cell_key,
    'urgency', 'high',
    'url', './'
  ));

  return NEW;
end;
$$;

drop trigger if exists on_schedule_change on public.schedules;
create trigger on_schedule_change
after insert or update on public.schedules
for each row execute function public.trg_schedule_change();

-- 5) Aviso al empezar cada clase, si hay tarea pendiente para ella -----
create or replace function public.notify_class_start(p_block_idx text, p_is_militar boolean)
returns void
language plpgsql
security definer
set search_path = public, private, extensions
as $$
declare
  v_col int := public.today_day_col();
  v_week int := public.current_week_number();
  v_key text;
  v_row public.schedules%rowtype;
  v_subject text;
begin
  if v_col < 0 or v_col > 4 then
    return; -- fin de semana, no hay clases
  end if;

  if p_is_militar then
    v_key := 'mil-' || v_col;
  else
    if v_col > 3 then
      return; -- viernes solo tiene Fase Militar
    end if;
    v_key := p_block_idx || '-' || v_col;
  end if;

  select * into v_row from public.schedules where week_number = v_week and cell_key = v_key;
  if not found or v_row.task is null or btrim(v_row.task) = '' then
    return; -- no hay tarea pendiente para esta clase
  end if;

  v_subject := coalesce(public.subject_name(v_key), 'Materia');

  perform private.notify_push(jsonb_build_object(
    'title', '🔔 Empieza ' || v_subject,
    'body', 'Tienes tarea pendiente: ' || v_row.task,
    'tag', 'class-' || v_key,
    'urgency', 'high',
    'url', './'
  ));
end;
$$;

-- 6) Resumen diario a las 5:30am (hora Bogota) --------------------------
create or replace function public.notify_daily_summary()
returns void
language plpgsql
security definer
set search_path = public, private, extensions
as $$
declare
  v_week int := public.current_week_number();
  v_col int := public.today_day_col();
  v_lines text := '';
  v_count int := 0;
  v_subject text;
  r record;
begin
  if v_col < 0 or v_col > 4 then
    return; -- fin de semana
  end if;

  for r in
    select * from public.schedules
    where week_number = v_week
      and task is not null and btrim(task) <> ''
      and (
        cell_key = 'mil-' || v_col
        or (v_col <= 3 and cell_key like '%-' || v_col::text and cell_key not like 'mil-%')
      )
    order by cell_key
  loop
    v_subject := coalesce(public.subject_name(r.cell_key), 'Materia');
    v_lines := v_lines || '• ' || v_subject || ': ' || r.task || E'\n';
    v_count := v_count + 1;
  end loop;

  if v_count = 0 then
    v_lines := 'No tienes tareas pendientes para hoy. ¡Buen día! 🎉';
  end if;

  perform private.notify_push(jsonb_build_object(
    'title', '☀️ Tareas de hoy (' || v_count || ')',
    'body', v_lines,
    'tag', 'daily-summary',
    'urgency', 'high',
    'url', './'
  ));
end;
$$;

-- 7) Programar los avisos con pg_cron -----------------------------------
-- Bogota es UTC-5 todo el año (sin horario de verano), por eso las horas
-- UTC de abajo son siempre "hora local + 5".
do $$
declare
  v_jobname text;
begin
  for v_jobname in
    select unnest(array[
      'daily-tasks-summary',
      'class-start-0','class-start-1','class-start-2','class-start-3',
      'class-start-4','class-start-5','class-start-6','class-start-7',
      'class-start-militar'
    ])
  loop
    perform cron.unschedule(jobid) from cron.job where jobname = v_jobname;
  end loop;
end $$;

-- Resumen diario: 5:30am Bogota = 10:30 UTC
select cron.schedule('daily-tasks-summary', '30 10 * * *',
  $$select public.notify_daily_summary();$$);

-- Inicio de cada bloque de clase, Lunes a Jueves (dow 1-4)
select cron.schedule('class-start-0', '0 12 * * 1-4',  $$select public.notify_class_start('0', false);$$); -- 7:00
